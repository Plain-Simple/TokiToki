package plainsimple.tokitoki;

public class PartOfSpeech {
    String gender, number, person, tense, base_form, grammatical_case, language;

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getPerson() {
        return person;
    }

    public void setPerson(String person) {
        this.person = person;
    }

    public String getTense() {
        return tense;
    }

    public void setTense(String tense) {
        this.tense = tense;
    }

    public String getBase_form() {
        return base_form;
    }

    public void setBase_form(String base_form) {
        this.base_form = base_form;
    }

    public String getGrammatical_case() {
        return grammatical_case;
    }

    public void setGrammatical_case(String grammatical_case) {
        this.grammatical_case = grammatical_case;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }
}
